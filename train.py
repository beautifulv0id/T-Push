def main():
    args = parse_args()
    init_wandb(args)
    dataset_path = args.dataset_path
    model_save_dir = wandb.run.dir

    # download demonstration data from Google Drive
    if not os.path.isfile(dataset_path):
        id = "1KY1InLurpMvJDRb14L9NlXT_fEsCvVUq&confirm=t"
        gdown.download(id=id, output=dataset_path, quiet=False)

    # parameters
    batch_size = args.batch_size
    pred_horizon = 16
    obs_horizon = 2
    action_horizon = 8
    #|o|o|                             observations: 2
    #| |a|a|a|a|a|a|a|a|               actions executed: 8
    #|p|p|p|p|p|p|p|p|p|p|p|p|p|p|p|p| actions predicted: 16

    # create dataset from file
    dataset = PushTImageDataset(
        dataset_path=dataset_path,
        pred_horizon=pred_horizon,
        obs_horizon=obs_horizon,
        action_horizon=action_horizon
    )
    # save training data statistics (min, max) for each dim
    stats = dataset.stats

    # create dataloader
    dataloader = torch.utils.data.DataLoader(
        dataset,
        batch_size=batch_size,
        num_workers=4,
        shuffle=True,
        # accelerate cpu-gpu transfer
        pin_memory=True,
        # don't kill worker process afte each epoch
        persistent_workers=True
    )


    num_epochs = args.num_epochs
    embedding_dim = args.embedding_dim
    action_dim = 2
    device = torch.device('cuda')
    num_diffusion_iters = 100
    noise_scheduler = DDPMScheduler(
        num_train_timesteps=num_diffusion_iters,
        # the choise of beta schedule has big impact on performance
        # we found squared cosine works the best
        beta_schedule='squaredcos_cap_v2',
        # clip output to [-1,1] to improve stability
        clip_sample=True,
        # our network predicts noise (instead of denoised action)
        prediction_type='epsilon'
    )
    obs_horizon = 2
    model = DiffusionTransformerImage(action_dim=action_dim,
                                    obs_horizon=obs_horizon,
                                    pred_horizon=pred_horizon,
                                    noise_scheduler=noise_scheduler,
                                    vis_backbone='clip',
                                    re_cross_attn_layer_within=3,
                                    re_cross_attn_num_heads_within=3,
                                    re_cross_attn_layer_across=3,
                                    re_cross_attn_num_heads_across=3,
                                    embedding_dim=embedding_dim,
                                    device='cuda')
    start_epoch = 0
    best_loss = np.inf


    model = model.to(device)

    ema_model = copy.deepcopy(model)
    _ = model.to(device)
    _ = ema_model.to(device)
    ema = EMAModel(ema_model)

    optimizer = torch.optim.AdamW(
        params=model.parameters(),
        lr=1e-4, weight_decay=1e-6)

    lr_scheduler = get_scheduler(
        name='cosine',
        optimizer=optimizer,
        num_warmup_steps=500,
        num_training_steps=len(dataloader) * num_epochs
    ) 

    if args.resume_run_id is not None:
        print('Resuming from run: ', args.resume_run_id)
        ckptn = wandb.restore('latest_model.ckpt').name
        ckpt = torch.load(ckptn)
        start_epoch = ckpt['epoch_idx'] + 1
        model.load_state_dict(ckpt['model'])
        best_loss = ckpt['loss']
        ema_model.load_state_dict(ckpt['ema_model'])
        optimizer.load_state_dict(ckpt['optimizer'])
        lr_scheduler.load_state_dict(ckpt['lr_scheduler'])

    print('Start training from epoch: ', start_epoch)
    log_buffer = list()
    with tqdm(range(start_epoch, num_epochs), desc='Epoch') as tglobal:
        losses = list()
        for epoch_idx in tglobal:
            epoch_loss = list()
            # batch loop
            with tqdm(dataloader, desc='Batch', leave=False) as tepoch:
                for item in tepoch:
                    loss = model.compute_loss(item)
                    loss.backward()
                    optimizer.step()
                    optimizer.zero_grad()
                    # step lr scheduler every batch
                    # this is different from standard pytorch behavior
                    lr_scheduler.step()

                    # update Exponential Moving Average of the model weights
                    ema.step(model)

                    # logging
                    loss_cpu = loss.item()
                    epoch_loss.append(loss_cpu)
                    tepoch.set_postfix(loss=loss_cpu)
            epoch_loss = np.mean(epoch_loss)
            losses.append(epoch_loss)
            tglobal.set_postfix(loss=epoch_loss)
            log_buffer.append({'loss': epoch_loss})
            
            if epoch_idx % args.save_model_every == 0:
                for metrics in log_buffer:
                    wandb.log(metrics)
                log_buffer = list()
                print('Saving model...')
                ckpt = {
                    'model': model.state_dict(),
                    'ema_model': ema.averaged_model.state_dict(),
                    'optimizer': optimizer.state_dict(),
                    'lr_scheduler': lr_scheduler.state_dict(),
                    'epoch_idx': epoch_idx,
                    'loss': epoch_loss
                }
                torch.save(ckpt, os.path.join(model_save_dir, 'latest_model.ckpt'))
                wandb.save(os.path.join(model_save_dir, 'latest_model.ckpt'))
                if epoch_loss < best_loss:
                    best_loss = epoch_loss
                    wandb.save(os.path.join(model_save_dir, 'best_model.ckpt'))
                

    ema_model = ema.averaged_model

    max_steps = 200
    env = PushTImageEnv()
    # use a seed >200 to avoid initial states seen in the training dataset
    env.seed(100000)

    # get first observation
    obs, info = env.reset()

    # keep a queue of last 2 steps of observations
    obs_deque = collections.deque(
        [obs] * obs_horizon, maxlen=obs_horizon)
    # save visualization and rewards
    imgs = [env.render(mode='rgb_array')]
    rewards = list()
    done = False
    step_idx = 0

    with tqdm(total=max_steps, desc="Eval PushTImageEnv") as pbar:
        while not done:
            B = 1
            images = np.stack([x['image'] for x in obs_deque])
            images = torch.from_numpy(images).to(device).unsqueeze(0).float()
            agent_pos = np.stack([x['agent_pos'] for x in obs_deque])
            agent_pos = torch.from_numpy(agent_pos).to(device).unsqueeze(0).float()
            
            obs_dict = {
                'image': images,
                'agent_pos': agent_pos
            }

            # predict action
            with torch.no_grad():
                action = ema_model.predict_action(obs_dict)
                action = action.cpu().numpy()
            
            naction = action[0]
            naction = agent_pos[0, -1].cpu().numpy() + naction
            action_pred = unnormalize_data(naction, stats['action'])
            
            # only take action_horizon number of actions
            start = obs_horizon - 1
            end = start + action_horizon
            action = action_pred[start:end,:]
            # (action_horizon, action_dim)

            for i in range(len(action)):
                # stepping env
                obs, reward, done, _, info = env.step(action[i])
                # save observations
                obs_deque.append(obs)
                # and reward/vis
                rewards.append(reward)
                imgs.append(env.render(mode='rgb_array'))

                # update progress bar
                step_idx += 1
                pbar.update(1)
                pbar.set_postfix(reward=reward)
                if step_idx > max_steps:
                    done = True
                if done:
                    break

    # print out the maximum target coverage
    print('Score: ', max(rewards))

    # visualize
    from IPython.display import Video
    vwrite('vis.mp4', imgs)
    Video('vis.mp4', embed=True, width=256, height=256)
    
    
if __name__ == '__main__':
    main()